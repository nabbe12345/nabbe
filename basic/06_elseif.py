# 콜라 , 생수 , 오렌지주스 , 식혜 , 이온음료
item = '커피'

if item == '콜라':                # 자바는 이퀄스  ,  파이썬은 ==
    print(f'{item}가 나왔습니다')  #f-str
elif item == '생수':              # 자바는 else if , 파이썬은 elif
    print(f'{item}가 나왔습니다')

elif item == '오렌지주스':
    print(f'{item}가 나왔습니다')

elif item == '식혜':
    print(f'{item}가 나왔습니다')

elif item == '이온음료':
    print(f'{item}가 나왔습니다')
else:
    print(f'{item}는 없습ㄴ디ㅏ')

