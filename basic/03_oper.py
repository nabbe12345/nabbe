result = 4+3        #최초 선언을 찾아야 한다 (변수)
print(result)

result = 4-3
print(result)

result = 7/2
print(result)   # 나누기: 소숫점도 반영해서 입력
                #변수 그릇이 커서 소숫점까지 다 표현


result = 7//2  # // = 소숫점 버림 (파이썬에만 있는 연산자)
print(result)


result = 7%2   # 나머지 연산
print(result)


result = 7**2  # 제곱연산
print(result)  #  tip 문제 풀이 할시 숫자가 너무 크게 잡히지 않게




